<small>First Type in all Names of Players on the top left list.</small> <br>
<small>Type in «+» for any Computer Players.</small>
<p>Roll all 6 dice on your turn by clicking anywhere on the screen. <br>
    Look at the dice you just rolled to find the ones that score points. <br>
    <br>
    The ones who count are 1s, 5s, and sets of the same dice. <br>
    Dice showing a 1 or 5 score by themselves, but all other values score points if you roll a set of 3 or more. The point values of each scoring dice are: <br>
    <br>
    If a player get a pair of 5s, its a choice of taking the two fives, or turn one 5 to a 1 and trow the other again. <br>
    <br>
    Single 5: 50 points<br>
    Single 1: 100 points<br>
    Three 2s: 200 points<br>
    Three 3s: 300 points<br>
    Three 4s: 400 points<br>
    Three 5s: 500 points<br>
    Three 6s: 600 points<br>
    Three 1s: 1’000 points<br>
    <br>
    For each extra die with the same value as a 3-of-a-kind, you earn double the amount of points. So, if you roll three 2s, you gain 200 points. If you roll four 2s in the same roll, the points double to 400. After that, five 2s are worth 800 points and six 2s are worth 1,600 points.<br>
    On your next turn you still can select the ones who where 3 ore more in the last throw. As long as you can select these number in the throw, as long this opportunity remains. <br>
    <br>
    Select at least 1 die that scores to continue your turn. <br>
    If you want to continue your turn, select at least 1 scoring die.<br>
    <br>
    You won’t roll any dice you set aside again for the rest of your turn.<br>
    Even if you have multiple dice that score, you only need to set 1 of them aside. That way, you have more dice to reroll and have a better chance at earning more points.<br>
    <br>
    Earn at least 1’000 points on your first turn to get «on the board». <br>
    Each person needs to earn 1,000 points when it’s their turn to begin officially scoring.<br>
    After your first turn, you can earn as few or as many points as you’d like.<br>
    You wont be able to «Take Points Button» unless you score 1’000<br>
    <br>
    After each time you set aside scoring dice, choose if you want to continue rolling the remaining dice to bank more points, or call it quits for your turn to add the points to your score. Once you decide to stop, your turn is over and the next player takes theirs.<br>
    Stop rolling when you’re satisfied with your score for the round, bay click on the «Take Points Button». <br>
    The next player play along with the remaining Points.<br>
    <br>
    If you wont be able to take any Point after you trow the Dice, the counter will set to 0 and its the next persons turn.<br>
    End your turn by clicking the Screen again, the Dice will placed in the center again.